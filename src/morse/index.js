  function morse(text) {
    if (typeof(text) != "string"){
      throw new Error ("Please provide a morse string")
    }
    let textConverted = [];
//array.map(function(currentValue, index, arr), thisValue)
let wordSplit =text.split("   ")
    wordSplit.map(function (word) {
      let letterSplit = word.split(" ")
        letterSplit.map(function (letter) {
            textConverted.push(MORSE_CODE[letter]);
        });
        textConverted.push(" ");
    });
     
    let final = textConverted.join("").trim()
return final
  }

  

